package maze

type Direction int

const (
	North Direction = iota
	South
	East
	West
)

func (d Direction) Opposite() Direction {
	switch d {
	case North:
		return South
	case South:
		return North
	case East:
		return West
	case West:
		return East
	}

	panic("Invalid direction")
}

func (d Direction) String() string {
	switch d {
	case North:
		return "North"
	case South:
		return "South"
	case East:
		return "East"
	case West:
		return "West"
	}

	panic("Invalid direction")
}
